class CardValues {
  String name = "";
  String storageName = "";

  void setName(String name) {
    this.name = name;
  }

  String getName() {
    return name;
  }

  void setStorage(String storageName) {
    this.storageName = storageName;
  }

  String getStorage() {
    return storageName;
  }
}
