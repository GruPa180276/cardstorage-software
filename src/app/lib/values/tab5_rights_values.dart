class Tab5RightsValuesProvider {
  String callerName = "";
  String rightsName = "";
  String accessName = "";

  void setCallerName(String callerName) {
    this.callerName = callerName;
  }

  String getCallerName() {
    return callerName;
  }

  void setRightsName(String rightsName) {
    this.rightsName = rightsName;
  }

  String getRightsName() {
    return rightsName;
  }

  void setAccessName(String accessName) {
    this.accessName = accessName;
  }

  String getAccessName() {
    return accessName;
  }
}
